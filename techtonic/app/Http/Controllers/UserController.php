    {
        $user = User::with('orders')
                    ->where('is_admin', 0)
                    ->find($request->email);
        $orders = $user->orders;
        
        return view('admin.orders', [
            'orders' => $orders,
            'title' => $user->first_name . "'s Orders"
        ]);
    }

